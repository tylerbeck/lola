(function( lola ) {
	var $ = lola;

	var Command = {

		//==================================================================
		// Attributes
		//==================================================================
		//command registration namespace
		name: "default",

		//==================================================================
		// Methods
		//==================================================================

		//------------------------------------------------------------------
		// init
		//------------------------------------------------------------------
		init: function() {
			return this;
		},

		//------------------------------------------------------------------
		// execute
		//------------------------------------------------------------------
		execute: function( params ) {
			//command code

			try{
				lola.cmd.result( this, {data:true} );
			}
			catch(e){
				lola.cmd.result( this, "fault" );
			}
		},


	};
	lola.cmd.register( Command );
})( lola );
